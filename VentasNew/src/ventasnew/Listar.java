    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
      int fila = tblLista.getSelectedRow();
      if(fila != -1)
      {
          int id = (int)tblLista.getModel().getValueAt(fila, 0);
          Controlador c = new Controlador();
          boolean elimino = c.eliminarVenta(id);
          if (elimino) {
              JOptionPane.showMessageDialog(this, "se elimino");
          }
          else{
              JOptionPane.showMessageDialog(this, "no elimino");
          }
      }
      else{
          JOptionPane.showMessageDialog(this, "Selecciona una venta a eliminar");
      }
       
    }//GEN-LAST:event_btnEliminarActionPerformed

