    private void llenarTabla()
    {
        Controlador c = new Controlador();
        ArrayList <Venta> lista = c.obtenerVentas();
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.setColumnIdentifiers(new String[]{"Fecha","Descuento","Cantidad","Producto"});
        for(Venta v : lista){
        modelo.addRow(new Object []{v.getId(),v.getFecha(),v.getDescuento(),v.getCantidad(),v.getProducto().getNombre()});            
        }
        tblLista.setModel(modelo);
        
    }
